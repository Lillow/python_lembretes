# Listas (list)
# Coleção ordenada de itens:
frutas = ["maçã", "banana", "cereja"]
print(type(frutas))  # <class 'list'>
print(frutas[1])

#Tuplas (tuple)
# Coleção ordenada e imutável de itens:
coordenadas = (10.0, 20.0, 10.0)
print(type(coordenadas))  # <class 'tuple'>
print(coordenadas[1])

# Conjuntos (set)
# Coleção não ordenada de itens únicos:
conjunto = {1, 2, 3, 4, 4}
print(type(conjunto))  # <class 'set'>
print(conjunto)  # {1, 2, 3, 4}

#Dicionários (dict)
# Coleção de pares chave-valor:
pessoa = {"nome": "João", "idade": 25}
print(type(pessoa))  # <class 'dict'>

