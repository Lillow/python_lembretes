# E lógico (and)
# Retorna True se ambos os operandos forem verdadeiros:
a = True
b = False
print(a and b)  # False
print(a and True)  # True

# Ou lógico (or)
# Retorna True se pelo menos um dos operandos for verdadeiro:
a = True
b = False
print(a or b)  # True
print(b or False)  # False

# Não lógico (not)
# Inverte o valor lógico do operando:
a = True
print(not a)  # False
print(not False)  # True