# Operações com Inteiros e Floats:
a = 10
b = 3
print(a + b)  # Adição
print(a - b)  # Subtração
print(a * b)  # Multiplicação
print(a / b)  # Divisão
print(a // b) # Divisão inteira
print(a % b)  # Módulo
print(a ** b) # Exponenciação

# Operações com Strings:
s1 = "Olá"
s2 = "Mundo"
print(s1 + " " + s2)  # Concatenação
print(s1 * 3)         # Repetição
print(len(s1))        # Comprimento

# Operações com Listas:
lista = [1, 2, 3]
lista.append(4)  # Adicionar item
lista.remove(2)  # Remover item
print(lista[0])  # Acessar item
print(len(lista)) # Comprimento da lista